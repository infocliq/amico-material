import { z } from 'zod'

export const projectStatusSchema = z.union([
  z.literal('none'),
  z.literal('next'),
  z.literal('preparing'),
  z.literal('staged'),
  z.literal('done'),
])
export type ProjectStatus = z.infer<typeof projectStatusSchema>

const _projectSchema = z.object({
  id: z.string(),
  salesOrder: z.string().catch(''),
  workOrder: z.string().catch(''),
  productionOrder: z.string().catch(''),
  asBuiltDate: z.coerce.date().optional(),
  shipDate: z.coerce.date().optional(),
  supervisors: z.string().catch(''),
  line: z.string().catch(''),
  plNumber: z.string().catch(''),
  name: z.string(),
  product: z.string().catch(''),
  productionStart: z.coerce.date().optional(),
  productionEnd: z.coerce.date().optional(),
  phase: z.string().catch(''),
  drawing: z.string().catch(''),
  supportStart: z.coerce.date().optional(),
  panelMaterial: z.string().catch(''),
  hollyMaterial: z.string().catch(''),
  blueFolderFredG: z.string().catch(''),
  firstOffJay: z.string().catch(''),
  uvKitting: z.string().catch(''),
  woodshopPanel: z.string().catch(''),
  robertoSub: z.string().catch(''),
  alexSumelEmtPipe: z.string().catch(''),
  jaysonBox: z.string().catch(''),
  jaysonWire: z.string().catch(''),
  arnoldCut: z.string().catch(''),
  arnoldCnc: z.string().catch(''),
  kevinMaterialHandling: z.string().catch(''),
  checklistId: z.string().optional(),
  checklist: z.any().optional(),
  status: projectStatusSchema.catch('none'),
  assignee: z.string().catch(''),
  tasks: z.array(z.string()).catch([]),
  createdAt: z.coerce.date(),
  updatedAt: z.coerce.date(),
})
export type Project = z.infer<typeof _projectSchema>
